<template>
  <input
    :value="modelValue"
    @input="updateInput"
    class="w-full h-10 p-5 rounded border border-solid border-teal-400"
    type="text"
  />
</template>

<script>
export default {
  name: "my-input",
  props: {
    modelValue: [String, Number],
  },
  methods: {
    updateInput(event) {
      this.$emit("update:modelValue", event.target.value);
    },
  },
};
</script>

